import 'package:flutter/material.dart';

enum TermsType { termsOfService, privacyPolicy }

class TermsDetailScreen extends StatelessWidget {
  final TermsType type;

  const TermsDetailScreen({super.key, required this.type});

  String get _title {
    switch (type) {
      case TermsType.termsOfService:
        return 'JAM 이용약관';
      case TermsType.privacyPolicy:
        return '개인정보 처리방침';
    }
  }

  String get _content {
    switch (type) {
      case TermsType.termsOfService:
        return _termsOfServiceContent;
      case TermsType.privacyPolicy:
        return _privacyPolicyContent;
    }
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Text(
          _title,
          style: const TextStyle(
            fontSize: 16,
            fontWeight: FontWeight.w500,
          ),
        ),
        forceMaterialTransparency: true,
        elevation: 0,
        iconTheme: const IconThemeData(
          size: 20,
        ),
      ),
      body: SingleChildScrollView(
        padding: const EdgeInsets.all(20),
        child: Text(
          _content,
          style: const TextStyle(
            fontSize: 14,
            height: 1.6,
          ),
        ),
      ),
    );
  }
}

